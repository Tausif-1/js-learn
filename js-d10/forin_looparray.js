// for in loop in array :-- it is not used widely . and it use to print index no of values. 


const fruits = ["apple", "mango","banana","grapes"];

for(let index in fruits){
    console.log(index);
}

// it prints the index no of variable values .

for(let i in fruits){
    console.log(i);
}

// it do the same it also prints the index no of values.


