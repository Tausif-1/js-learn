// string indexing :-

// string - it is ordered sequences of character of data.
//indexing - it allows you to access individual characters in a string directly by using numeric value .

//indexing is always start from o [zero].

//ex:- 
let firstName = "tausif";
console.log(firstName[3])

// it prints 's' character only .

// t a u s i f  - string
// 0 1 2 3 4 5 - indexing

//length of strinf :- it is use to know the length of a string.

//length is always starts from 1 .

// if we use extra-spaces it also count in length.

//ex:- 
let Firstname = "tausif";
console.log(Firstname.length) // it prints 6 because our string length is 6.
console.log(Firstname[Firstname.length-1]) // it prints the last charchter of string and it prints 'f'.

//  t   a  u  s  i  f   - string
//  1   2  3  4  5  6   - length index
