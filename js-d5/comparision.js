// comparision operator :-- it compares values and it returns true or false .

// some examples of comparision operators ---

// # 1. double equalto [==]:-

// it is use to check only values that returns true or false and it doesn't check their datatypes.

// ex:- 
 let num1 = 7;
 let num2 = 7;
 console.log(num1==num2);

//  it returns true .

// what if we change their datatypes is it returns true or false?

// ans:---

let Num1 = "7";
let Num2 = 7;
console.log(Num1==Num2);

// it also returns true ans it check their values not their datatypes.


// # 2. triple equalto [===]:---

// it is use to the values and also their datatypes.

// ex:--

let age1 = "7";
let age2 = 7;
console.log(age1===age2);

// it returns false because it check their values and also their datatypes.

//  # 3. != [not equalto] :--
//                             it checks only values not datatypes.


// ex:--

let Age1 = "17";
let Age2 = 17;
console.log(Age1 != Age2);

// it returns false and it check only values not their datatypes.


let Any1 = "17";
let Any2 = 18;
console.log(Any1 != Any2);

// it returns true  it check only values not datatypes.


//  # 4. !== [not double equalto] :----

//        it checks both values and their datatypes.

// ex:--- 

let any1 = "17";
let any2 = 17;
console.log(any1 !== any2);

// it returns true , it checks both values and their datatypes .


