// ternary 0r conditional operator :-- 

//   ternary or conditional operator that shows the values according to their condition.


// ex:---

let age = 15;
let drink;

if(age>=14){
    drink = "coffee";
}else{
    drink = "milk";
}

console.log(drink);

// it prints coffee .

// using ternary operator ----

let Age = 5;
let Drink = Age>=7 ? "coffee" : "milk";
console.log(Drink); 

// it prints milk . it is advance method of using ternary operator.

// note :- we can use [ ? ] to check the condition is true ,if the condition is false we use [ : ] to know the condition is false .

